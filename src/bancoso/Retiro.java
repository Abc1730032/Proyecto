    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        Retiro = Integer.parseInt(txtCantidad.getText());
        if (Saldo() >= Retiro) {
            while (Billetes1000 > Cont1000 && (Actual + 1000) <= Retiro) {
                Actual += 1000;
                Cont1000++;
            }
            while (Billetes500 > Cont500 && (Actual + 500) <= Retiro) {
                Actual += 500;
                Cont500++;
            }
            while (Billetes200 > Cont200 && (Actual + 200) <= Retiro) {
                Actual += 200;
                Cont200++;
            }
            while (Billetes100 > Cont100 && (Actual + 100) <= Retiro) {
                Actual += 100;
                Cont100++;
            }
            while (Billetes50 > Cont50 && (Actual + 50) <= Retiro) {
                Actual += 50;
                Cont50++;
            }
            while (Billetes20 > Cont20 && (Actual + 20) <= Retiro) {
                Actual += 20;
                Cont20++;
            }
            if (Actual == Retiro) {
                JOptionPane.showMessageDialog(null, "<html><h2>Retiro Exitoso : $" + Retiro + ".00</h2></html>",
                        "Banco - BBVA", JOptionPane.INFORMATION_MESSAGE, icon);
                Billetes1000 -= Cont1000;
                Billetes500 -= Cont500;
                Billetes200 -= Cont200;
                Billetes100 -= Cont100;
                Billetes50 -= Cont50;
                Billetes20 -= Cont20;
                try {
                    Statement stm = new Conexion().Conexion().createStatement();
                    stm.executeUpdate("update Boveda set Cantidad=" + Billetes20 + " where Denominacion=20");
                    stm.executeUpdate("update Boveda set Cantidad=" + Billetes50 + " where Denominacion=50");
                    stm.executeUpdate("update Boveda set Cantidad=" + Billetes100 + " where Denominacion=100");
                    stm.executeUpdate("update Boveda set Cantidad=" + Billetes200 + " where Denominacion=200");
                    stm.executeUpdate("update Boveda set Cantidad=" + Billetes500 + " where Denominacion=500");
                    stm.executeUpdate("update Boveda set Cantidad=" + Billetes1000 + " where Denominacion=1000");
                    int s = Saldo() - Retiro;
                    stm.executeUpdate("update Usuarios set Saldo=" + s + " where Cuenta=\'" + txtCuenta.getText() + "\'");
                } catch (Exception e) {
                }
            } else {
                JOptionPane.showMessageDialog(null, "<html><h2>Lo sentimos, no contamos con</h2><h2>el Efectivo Suficiente.</h2></html>",
                        "Banco - BBVA", JOptionPane.INFORMATION_MESSAGE, icon);
            }
        } else {
            JOptionPane.showMessageDialog(null, "<html><h2>Error, no cuentas con</h2><h2>el Saldo Suficiente.</h2></html>",
                    "Banco - BBVA", JOptionPane.INFORMATION_MESSAGE, icon);
        }
        txtCantidad.setText("0");
    }//GEN-LAST:event_btnRetirarActionPerformed

