    public int Boveda(int a) {
        int v = 0;
        try {
            Statement stm = new Conexion().Conexion().createStatement();
            ResultSet rs = stm.executeQuery("select * from Boveda where Denominacion=" + a);
            while (rs.next()) {
                v = rs.getInt("Cantidad");
            }
        } catch (Exception e) {
        }
        return v;
    }

    public int Saldo() {
        int v = 0;
        try {
            Statement stm = new Conexion().Conexion().createStatement();
            ResultSet rs = stm.executeQuery("select * from Usuarios where Cuenta=\'" + txtCuenta.getText() + "\'");
            while (rs.next()) {
                v = rs.getInt("Saldo");
            }
        } catch (Exception e) {
        }
        return v;
    }

