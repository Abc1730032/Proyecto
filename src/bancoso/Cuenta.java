    public void Tipo() {
        int fin = 0;
        
        //Consulta el valor maximo del rango para generar numero de cuenta
        try {
            Statement stm = new Conexion().Conexion().createStatement();
            ResultSet rs = stm.executeQuery("select max(fin) as valor from tclientes");
            while (rs.next()) {
                fin = rs.getInt("valor");
            }
        } catch (Exception e) {
        }
        String cuenta = "", nip = "";
        for (int i = 0; i < 12; i++) {
            cuenta += (int) Math.floor(Math.random() * (10));
        }
        cuenta += validacion(fin);//Concatena los doce digitos iniciales con los 4 especiales
        for (int i = 0; i < 4; i++) {
            nip += (int) Math.floor(Math.random() * (10));
        }
        txtCuenta.setText(cuenta);
        txtNIP.setText(nip);
    }

    //Metodo para generar los ultimos digitos de la cuenta
    public String validacion(int fin) {
        String a = "";
        do {
            a = "";
            for (int j = 0; j < 4; j++) {
                a += (int) Math.floor(Math.random() * (10));
            }
        } while (Integer.parseInt(a) > fin);
        return a;
    }

