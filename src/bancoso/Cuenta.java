    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if (txtNombre.equals("") || txtCuenta.equals("") || txtNIP.equals("")) {
                JOptionPane.showMessageDialog(null, "<html><h2>Campos Incompletos</h2></html>",
                        "Banco - BBVA", JOptionPane.INFORMATION_MESSAGE, icon);
            } else {
                Statement stm = new Conexion().Conexion().createStatement();
                cliente(txtCuenta.getText());//Metodo de registro

                stm.executeUpdate("insert into Usuarios(cuenta,nip,saldo,nombre,tipo) values(\'" + txtCuenta.getText() + "\',\'" + txtNIP.getText() + "\',0,\'" + txtNombre.getText() + "\',\'U\')");
                JOptionPane.showMessageDialog(null, "<html><h2>Cuenta Registrada</h2></html>",
                        "Banco - BBVA", JOptionPane.INFORMATION_MESSAGE, icon);
                new Cuenta().setVisible(true);
                this.setVisible(false);
            }
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    //Metodo Agregado para Insertar Tipo de Clientes
    public void cliente(String cuenta) {
        try {
            Statement stm = new Conexion().Conexion().createStatement();
            Statement stm3 = new Conexion().Conexion().createStatement();
            ResultSet rs2 = stm3.executeQuery("select * from TClientes");
            while (rs2.next()) {
                int inicio = rs2.getInt("Inicio");
                int fin = rs2.getInt("Fin");
                String tipo = rs2.getString("Tipo");
                int dato=Integer.parseInt(txtCuenta.getText().substring(12, 16));
                if ( dato<= fin&& dato >= inicio) {
                    stm.executeUpdate("insert into " + tipo + "(cuenta,nombre) values(\'" + cuenta + "\',\'" + txtNombre.getText() + "\')");
                }
            }
        } catch (Exception e) {
        }
    }

