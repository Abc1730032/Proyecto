        if(!txt20.getText().equals("")){c20+=Integer.parseInt(txt20.getText());}
        if(!txt50.getText().equals("")){c50+=Integer.parseInt(txt50.getText());}
        if(!txt100.getText().equals("")){c100+=Integer.parseInt(txt100.getText());}
        if(!txt200.getText().equals("")){c200+=Integer.parseInt(txt200.getText());}
        if(!txt500.getText().equals("")){c500+=Integer.parseInt(txt500.getText());}
        if(!txt1000.getText().equals("")){c1000+=Integer.parseInt(txt1000.getText());}
        try {
            Statement stm = new Conexion().Conexion().createStatement();
            stm.executeUpdate("update Boveda set Cantidad="+c20+" where Denominacion=20");
            stm.executeUpdate("update Boveda set Cantidad="+c50+" where Denominacion=50");
            stm.executeUpdate("update Boveda set Cantidad="+c100+" where Denominacion=100");
            stm.executeUpdate("update Boveda set Cantidad="+c200+" where Denominacion=200");
            stm.executeUpdate("update Boveda set Cantidad="+c500+" where Denominacion=500");
            stm.executeUpdate("update Boveda set Cantidad="+c1000+" where Denominacion=1000");
            JOptionPane.showMessageDialog(null, "<html><h2>Boveda Actualizada</h2></html>",
                    "Banco - BBVA", JOptionPane.INFORMATION_MESSAGE, icon);
        } catch (Exception e) {
        }
        txt20.setText("0");
        txt50.setText("0");
        txt100.setText("0");
        txt200.setText("0");
        txt500.setText("0");
        txt1000.setText("0");
