    public int Contar(int n) {
        int a=0;
        try {
            Statement stm = new Conexion().Conexion().createStatement();
            ResultSet rs = stm.executeQuery("select * from Boveda where Denominacion="+n);
            while (rs.next()) {
                a = rs.getInt("Cantidad");
            }
        } catch (Exception e) {
        }
        return a;
    }
